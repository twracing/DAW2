export class Vehiculo {
    constructor(vehiculoId, matricula, marca, modelo, año, motor, propietario) {
        this.vehiculoId = vehiculoId;
        this.matricula = matricula;
        this.marca = marca;
        this.modelo = modelo;
        this.año = año;
        this.motor = motor;
        this.propietario = propietario;
    }
}
